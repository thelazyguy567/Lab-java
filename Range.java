public class Range{
    public static void main(String[] args){
        System.out.println(Math.max(5,10));
        System.out.println(Math.min(5,10));
        System.out.println(Math.sqrt(64));
        System.out.println(Math.abs(-10));
        System.out.println(Math.random());
        System.out.println(Math.sqrt(64));
    }    
}
